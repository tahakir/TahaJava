public class WakeUpTask_1 {
    public static void main(String[] args) {

        String weatherOfAtlanta="Sunny";

        if(weatherOfAtlanta.equals("Sunny")) {
            System.out.println("Code in Sunny Weather");
        }else if(weatherOfAtlanta.equals("Rainy")) {
            System.out.println("Code in Rainy Weather");
        }else if(weatherOfAtlanta.equals("Cloudy")) {
            System.out.println("Code in Rainy Weather");
        }else if(weatherOfAtlanta.equals("Snowy")) {
            System.out.println("Code in Snowy Weather");
        }else{
            System.out.println("Rain or Shine just keep coding anyway");


        }


    }



}
