package day11;

public class WarmUp1_LightOnOff_Char {
    public static void main(String[] args) {

        char targetOption='2';

        switch (targetOption){
            case '1':
            System.out.println("Hallway light is on");
            break;
            case '2':
                System.out.println("Restroom light is on");
                break;
            case '3':
                System.out.println("Kitchen light is on");
                break;
            case '4':
                System.out.println("Bedroom light is on");
                break;
            default:
                System.out.println("No light is on");
                }
            }

        }





