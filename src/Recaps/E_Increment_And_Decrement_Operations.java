package Recaps;

public class E_Increment_And_Decrement_Operations {

    public static void main(String[] args) {

//++var preincrement increase by 1 and use the new var value in the statement
//var++ postincrement increase var by 1 but use the original var value in the statement
//--var predecrement decrese by 1 and use the new var value in the statement
//var-- post decrement decrese var by 1 and use the new var value in the statement


        int apples=0;
        System.out.println(apples); //----0
        System.out.println(++apples);//----1
        System.out.println(apples++);//-----1
        System.out.println(apples);//------2
        System.out.println(--apples);//----1
        System.out.println(apples--);//----1
        System.out.println(apples);//-----0




    }
}
