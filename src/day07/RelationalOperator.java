package day07;

public class RelationalOperator {

    public static void main(String[] args) {

        // > >= < <= == !=
        //Relation oPERATOR IS USED TO COMPARE 2
        //AND RETURN TRUE OR FALSE
        //anything cation or expression
        //that return true or false (boolean expression)

        System.out.println(7>5);
        System.out.println(7<-19);
        System.out.println(5>=5);
        System.out.println(7>=8);
        System.out.println(4<=5);
        System.out.println(17>=8);

        //== is used to check for equality
        //and very very different from single=
        System.out.println(5==5);
        // != is used to check for inequality, ! in programming translate to NOT
        System.out.println(6!=10);
        System.out.println(6!=6);

        // == and != are questions, checking for equality or inequality






    }
}
